package packTiposImagen;


/**
 * 
 * Tipo de datos que representa un pixel en una imagen PPM
 *
 */
public class TipoPixel {
	/**
	 * La intensidad del tono de rojo (un valor entre 0 y 255).
	 */
	public int rojo;
	/**
	 * La intensidad del tono de verde (un valor entre 0 y 255).
	 */
	public int verde;
	/**
	 * La intensidad del tono de azul (un valor entre 0 y 255).
	 */
	public int azul;

}
